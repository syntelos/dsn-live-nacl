const FontGlyph* FontFutural::get(char ch){
    if (' ' >= ch){
        return &(GlyphSet[0]);
    }
    else {
        const unsigned int idx = (ch-' ');
        if (idx < FontFutural_COUNT){
            return &(GlyphSet[idx]);
        }
        else {
            return 0;
        }
    }
}

